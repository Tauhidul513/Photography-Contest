    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            ConnectMSSQL obj = new ConnectMSSQL();
            conn = obj.connectDB();
            s = conn.createStatement();
            int eid=Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(),0).toString());
            ResultSet resultSet = s.executeQuery("SELECT * FROM ENTRIES WHERE E_ID=" + eid);
            while (resultSet.next()) {
                String url = resultSet.getString("URL");
                DisplayImage(resultSet.getString("URL"));
            }
            resultSet = s.executeQuery("Select J_ID,score from scores where E_ID="+eid);
            model = (DefaultTableModel) jTable2.getModel();
            model.setRowCount(0);
            while (resultSet.next()) {
                model = (DefaultTableModel) jTable2.getModel();
                model.addRow(new Object[]{resultSet.getInt("J_ID"),resultSet.getInt("score")});
            }
            resultSet = s.executeQuery("select avg(score) as 'Average score',sum(score) as 'Sum' from scores where E_ID="+eid+" group by E_ID");
            while (resultSet.next()) {
                jLabel4.setText(resultSet.getString(1));
                jLabel5.setText(resultSet.getString(2));
            }
            resultSet = s.executeQuery("select *,height*width as 'Size' from entries where E_ID="+eid);
            model = (DefaultTableModel) jTable3.getModel();
            model.setRowCount(0);
            while (resultSet.next()) {
                model = (DefaultTableModel) jTable3.getModel();
                model.addRow(new Object[]{resultSet.getInt(1),resultSet.getString(2),resultSet.getString(3),resultSet.getString(4),resultSet.getFloat(5),resultSet.getFloat(6),resultSet.getInt(7),resultSet.getString(8),resultSet.getString(9),resultSet.getFloat(10)});
            }
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

