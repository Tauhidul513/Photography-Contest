    private void Refresh(){
     try {
            ConnectMSSQL obj = new ConnectMSSQL();
            conn = obj.connectDB();
            s = conn.createStatement();
            ResultSet resultSet = s.executeQuery("select E_ID from entries where P_ID="+pid);
            model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            while (resultSet.next()) {
                model = (DefaultTableModel) jTable1.getModel();
                model.addRow(new Object[]{resultSet.getInt("E_ID")});
            }
            
            conn.close();
        } catch (SQLException ex) {
            Logger.getLogger(ImageView.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }

    private void DisplayImage(String url) {
        try {
            BufferedImage myPicture = ImageIO.read(new File(url));
            Image dimg = myPicture.getScaledInstance(jLabel2.getWidth(), jLabel2.getHeight(),
                    Image.SCALE_SMOOTH);
            ImageIcon imageIcon = new ImageIcon(dimg);
            jLabel2.setIcon(imageIcon);
            jLabel2.setVisible(true);
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

